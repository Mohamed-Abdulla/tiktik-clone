export const allPostsQuery = () => {
  //Inside of query w're saying fetch * all documents that are of the type is equal to post.
  //In our case those are videos and we can order them in created at descending
  // so the newest ones are going to show at the top.
  //Then we specify what do we want to get from them.
  //I want to fetch the ID, caption, the video, which is going to contain acid including the ID in the URL.
  //We need to fetch the user and I posted by as well as the likes in all the comments and then we return that query.
  const query = `*[_type == "post"] | order(_createdAt desc){
    _id,
     caption,
       video{
        asset->{
          _id,
          url
        }
      },
      userId,
      postedBy->{
        _id,
        userName,
        image
      },
    likes,
    comments[]{
      comment,
      _key,
      postedBy->{
      _id,
      userName,
      image
    },
    }
  }`;

  return query;
};

